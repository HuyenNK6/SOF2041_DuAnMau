    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtMaNV.getText().isBlank() || txtMatKhau.getText().isBlank() || txtHoTen.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Vui lòng không để trông!!!");
            return;
        }
        if (!rdbQuanLy.isSelected() && !rdbNhanVien.isSelected()) {
            JOptionPane.showMessageDialog(this, "Phải chọn vai trò!!!");
            return;
        }

        addNew();
        loadTable();
    }//GEN-LAST:event_btnAddActionPerformed

