    //load dữ liệu lên table
    void loadTable() {
        _dtm = (DefaultTableModel) tblNhanVien.getModel();
        _dtm.setRowCount(0);
        _list = _repo.getAll();
        for (NhanVien nhanVien : _list) {
            _dtm.addRow(new Object[]{nhanVien.getMaNV(), nhanVien.getMatKhau(), nhanVien.getHoTen(), nhanVien.getVaiTro()});
        }
    }

    //load phần tử đầu tiên
    void getObject(int index) {
        _nhanVien = _list.get(index);
        txtMaNV.setText(String.valueOf(_nhanVien.getMaNV()));
        txtMatKhau.setText(_nhanVien.getMatKhau());
        txtHoTen.setText(_nhanVien.getHoTen());
        if (_nhanVien.getVaiTro().equalsIgnoreCase("Quan Ly")) {
            rdbQuanLy.setSelected(true);
        } else {
            rdbNhanVien.setSelected(true);
        }

    }

    void addNew() {
        _nhanVien = getValueJframeForm();
        System.out.println(_nhanVien.toString());
        _repo.add(_nhanVien);
    }

    NhanVien getValueJframeForm() {
        return new NhanVien(Integer.parseInt(txtMaNV.getText()),
                txtMatKhau.getText(),
                txtHoTen.getText(),
                rdbQuanLy.isSelected() ? "Quan ly" : "Nhan vien");
    }

